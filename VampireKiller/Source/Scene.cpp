void Scene::Update()
{
	if (IsKeyPressed(KEY_TWO))
	{
		LoadLevel(2);
	}
	else if (IsKeyPressed(KEY_ONE))
	{
		LoadLevel(1);
	}
	else if (IsKeyPressed(KEY_THREE))
	{
		LoadLevel(3);
	}
	else if (IsKeyPressed(KEY_FOUR))
	{
		LoadLevel(4);
	}
	else if (IsKeyPressed(KEY_FIVE))
	{
		LoadLevel(5);
	}
}
